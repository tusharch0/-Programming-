class Print_different_types_of_value
{
    public static void main(String args[])
    {
        int num;
        float b;
        char c;
        String s;
        num=100;
        b=1.234f;
        c='A';
        s="Hello world";
        System.out.println("Value of num: "+num);
        System.out.println("Value of b: "+b);
        System.out.println("Value of c: "+c);
        System.out.println("Value of s: "+s);
    }
}